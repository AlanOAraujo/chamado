package br.com.pagga.chamado.dao.filter;

public class UsuarioFilter {

	private Long id;
	
	private String nome;
	
	private String cpf;
	
	private String email;
	
	private String situacao;
	
	public UsuarioFilter withId(Long id) {
		this.id = id;
		return this;
	}

	public UsuarioFilter withNome(String nome) {
		this.nome = nome;
		return this;
	}
	
	public UsuarioFilter withCpf(String cpf) {
		this.cpf = cpf;
		return this;
	}
	
	public UsuarioFilter withEmail(String email) {
		this.email = email;
		return this;
	}
	
	public UsuarioFilter withSituacao(String situacao) {
		this.situacao = situacao;
		return this;
	}
	
	public Long getId() {
		return id;
	}

	public String getNome() {
		return nome;
	}

	public String getCpf() {
		return cpf;
	}

	public String getEmail() {
		return email;
	}

	public String getSituacao() {
		return situacao;
	}
	
}
