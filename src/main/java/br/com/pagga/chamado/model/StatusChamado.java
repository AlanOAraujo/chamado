package br.com.pagga.chamado.model;

public enum StatusChamado {

	ABERTO("Aberto"),
	EM_ATENDIMENTO("Atendimento"), 
	FECHADO("Finalizado");
	
	private String descricao;
	
	private StatusChamado(String decricao) {
		this.descricao = decricao;
	}

	public String getDescricao() {
		return descricao;
	}
}
